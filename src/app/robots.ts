import type { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
  const appUrl = process.env.NEXT_PUBLIC_APP_URL ?? "https://selene.app";

  return {
    rules: {
      userAgent: "*",
      allow: "/",
      disallow: ["/chat", "/api/"],
    },
    sitemap: `${appUrl}/sitemap.xml`,
  };
}
